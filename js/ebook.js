/* ----- 搜索 ----- */
function handleSearch(query) {
  const resultsEl = document.getElementById('ebookSearchResults');
  if (!resultsEl) return;

  if (!query || query.trim().length < 2) {
    resultsEl.classList.remove('visible');
    return;
  }

  const q = query.trim().toLowerCase();
  const results = [];

  for (const part of getCurrentBookData().parts) {
    for (const chapter of part.chapters) {
      for (const section of chapter.sections) {
        let matched = false;
        let preview = '';

        if (section.title.toLowerCase().includes(q)) {
          matched = true;
          preview = section.content.substring(0, 100);
        } else if (section.content.toLowerCase().includes(q)) {
          matched = true;
          const idx = section.content.toLowerCase().indexOf(q);
          const start = Math.max(0, idx - 30);
          const end = Math.min(section.content.length, idx + q.length + 70);
          preview = (start > 0 ? '...' : '') + section.content.substring(start, end) + (end < section.content.length ? '...' : '');
        } else if (section.keyPoints && section.keyPoints.some(kp => kp.toLowerCase().includes(q))) {
          matched = true;
          const kp = section.keyPoints.find(kp => kp.toLowerCase().includes(q));
          preview = '考点: ' + kp;
        }

        if (matched) {
          results.push({
            sectionId: section.id,
            title: section.title,
            preview: preview,
            chapterTitle: `第${chapter.number}章 ${chapter.title}`,
          });
        }
      }
    }
  }

  if (results.length === 0) {
    resultsEl.innerHTML = `<div class="ebook-search-empty">未找到相关内容</div>`;
  } else {
    let html = '';
    for (const r of results) {
      // 高亮关键词
      const highlightedTitle = highlightText(r.title, q);
      const highlightedPreview = highlightText(r.preview, q);
      html += `<div class="ebook-search-result-item" onclick="navigateToSection('${r.sectionId}'); closeSearch();">`;
      html += `<div class="ebook-search-result-title">${highlightedTitle}</div>`;
      html += `<div class="ebook-search-result-preview">${highlightedPreview}</div>`;
      html += `</div>`;
    }
    resultsEl.innerHTML = html;
  }

  resultsEl.classList.add('visible');
}

function highlightText(text, query) {
  if (!query) return text;
  const regex = new RegExp(`(${query.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')})`, 'gi');
  return text.replace(regex, '<mark>$1</mark>');
}

function closeSearch() {
  const resultsEl = document.getElementById('ebookSearchResults');
  const inputEl = document.getElementById('ebookSearchInput');
  if (resultsEl) resultsEl.classList.remove('visible');
  if (inputEl) inputEl.value = '';
}

