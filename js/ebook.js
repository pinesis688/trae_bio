async function downloadBookPdf(bookIndex) {
  if (typeof hasPermission === 'function' && !hasPermission('premium')) {
    showToast('下载PDF需要高级会员及以上权限');
    return;
  }
  const book = ALL_BOOKS[bookIndex];
  if (!book) return;
  if (!book.hasPdf) {
    showToast('该教材PDF尚未上传');
    return;
  }
  try {
    const { data, error } = await window.getSupabase().storage.from('bioquest-ebooks').createSignedUrl(book.pdfUrl, 3600);
    if (data && data.signedUrl) {
      window.open(data.signedUrl, '_blank');
    } else {
      throw new Error(error || '签名URL获取失败');
    }
  } catch (e) {
    console.warn('签名URL获取失败，尝试公共URL:', e);
    const publicUrl = window.getSupabase().storage.from('bioquest-ebooks').getPublicUrl(book.pdfUrl).data.publicUrl;
    if (publicUrl) {
      window.open(publicUrl, '_blank');
    } else {
      showToast('PDF下载失败，请稍后重试');
    }
  }
}

