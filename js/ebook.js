/* ----- 加载自定义书籍 ----- */
async function loadCustomEbooks() {
  // 从 localStorage 加载管理员上传的自定义书籍
  var customBooks = [];
  try {
    var raw = localStorage.getItem('bioquest_custom_ebooks');
    if (raw) customBooks = JSON.parse(raw);
  } catch(e) {}

  // 也尝试从 Supabase 加载
  if (typeof window.getSupabase === 'function') {
    var sb = window.getSupabase();
    if (sb) {
      try {
        var { data, error } = await sb.from('ebook_pdfs').select('*').order('created_at', { ascending: true });
        if (data && data.length > 0) {
          // 合并 Supabase 数据到 customBooks（以 Supabase 为准）
          var localMap = {};
          customBooks.forEach(function(b) { localMap[b.id] = b; });
          data.forEach(function(b) {
            localMap[b.id] = {
              id: b.id,
              title: b.title,
              author: b.author || '未知',
              category: b.category || '其他',
              filePath: b.file_path,
              uploadDate: b.created_at
            };
          });
          customBooks = Object.values(localMap);
        }
      } catch(e) {
        // ebook_pdfs 表可能不存在，忽略
      }
    }
  }

  if (customBooks.length === 0) return;

  // 将自定义书籍添加到 ALL_BOOKS
  var startId = ALL_BOOKS.length;
  customBooks.forEach(function(book, idx) {
    var bookId = startId + idx;
    // 检查是否已添加（避免重复）
    var exists = ALL_BOOKS.some(function(b) { return b.id === bookId && b.title === book.title && b._isCustom; });
    if (!exists) {
      ALL_BOOKS.push({
        id: bookId,
        title: book.title,
        edition: book.category || '自定义',
        author: book.author || '未知',
        pdfUrl: book.filePath,
        hasPdf: true,
        _isCustom: true,
        _customId: book.id,
        parts: [
          {
            id: 'custom-' + bookId + '-part1',
            title: 'PDF 全文',
            chapters: [
              {
                id: 'custom-' + bookId + '-ch1',
                number: 1,
                title: '下载阅读原版PDF',
                sections: [
                  {
                    id: 'custom-' + bookId + '-sec1',
                    title: '原版教材',
                    content: '此书为管理员上传的原版PDF教材，点击下方按钮下载阅读。\n\n**书名**：' + book.title + '\n**作者/版本**：' + (book.author || '未知') + '\n**分类**：' + (book.category || '其他') + '\n\n> 下载PDF需要高级会员及以上权限。',
                    keyPoints: ['原版PDF教材', '管理员上传'],
                    relatedTopics: []
                  }
                ]
              }
            ]
          }
        ]
      });
    }
  });

  // 更新书籍选择器
  var selector = document.getElementById('ebookBookSelector');
  if (selector) {
    // 移除旧的自定义选项
    selector.querySelectorAll('option[data-custom]').forEach(function(opt) { opt.remove(); });
    // 添加自定义书籍选项
    customBooks.forEach(function(book, idx) {
      var bookId = startId + idx;
      var opt = document.createElement('option');
      opt.value = bookId;
      opt.textContent = book.title;
      opt.setAttribute('data-custom', 'true');
      selector.appendChild(opt);
    });
  }
}

