# ================== 主流程 ==================
def build_questions():
    qs = []
    for proto in PROTOTYPES:
        for stem in proto["stems"]:
            for opts, answer, analysis in proto["option_groups"]:
                qs.append({
                    "stem": stem,
                    "options": {"A": opts[0], "B": opts[1], "C": opts[2], "D": opts[3]},
                    "answer": answer,
                    "analysis": analysis,
                    "knowledge": [proto["subject"], proto["concept"]],
                    "difficulty": proto["difficulty"],
                    "target": proto["target"],
                    "subject": proto["subject"],
                    "concept": proto["concept"],
                    "module": proto["module"],
                    "tags": [proto["module"], proto["subject"], proto["concept"], proto["target"]],
                })
    for proto in CHART_PROTOTYPES:
        for stem in proto["stems"]:
            qs.append({
                "stem": stem,
                "options": {"A": proto["options"][0], "B": proto["options"][1], "C": proto["options"][2], "D": proto["options"][3]},
                "answer": proto["answer"],
                "analysis": proto["analysis"],
                "chart": proto["chart"],
                "knowledge": [proto["subject"], proto["concept"]],
                "difficulty": proto["difficulty"],
                "target": proto["target"],
                "subject": proto["subject"],
                "concept": proto["concept"],
                "module": proto["module"],
                "tags": [proto["module"], proto["subject"], proto["concept"], "图表题"],
            })
    return qs


def main():
    pool = load()
    existing = {(q.get("stem"), q.get("concept")) for q in pool}
    new_qs = build_questions()
    added = 0
    for q in new_qs:
        key = (q["stem"], q["concept"])
        if key in existing:
            continue
        existing.add(key)
        q["id"] = f"local_{int(time.time()*1000)}_{random.randint(0,9999):04d}"
        q["weight"] = 1.0
        q["fb_good"] = 0
        q["fb_bad"] = 0
        q["created_at"] = time.time()
        pool.append(q)
        added += 1
    save(pool)
    targets = {}
    diffs = {}
    modules = {}
    for q in pool:
        t = q.get("target", "(none)")
        targets[t] = targets.get(t, 0) + 1
        d = str(q.get("difficulty"))
        diffs[d] = diffs.get(d, 0) + 1
        m = str(q.get("module"))
        modules[m] = modules.get(m, 0) + 1
    print(f"新增 {added} 题，pool 总数={len(pool)}")
    print(f"targets={targets}")
    print(f"difficulties={diffs}")
    print(f"modules={modules}")
    print(f"chart={sum(1 for q in pool if q.get('chart'))}")


if __name__ == "__main__":
    main()
