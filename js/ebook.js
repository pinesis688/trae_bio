/* ----- 工具函数 ----- */
function loadFromStorage(key, fallback) {
  try {
    const val = localStorage.getItem(key);
    return val !== null ? JSON.parse(val) : fallback;
  } catch {
    return fallback;
  }
}

function saveToStorage(key, value) {
  try {
    localStorage.setItem(key, JSON.stringify(value));
  } catch { /* ignore */ }
}

function getReadSections() {
  return loadFromStorage(STORAGE_KEYS.READ_SECTIONS, []);
}

function markAsRead(sectionId) {
  const reads = getReadSections();
  if (!reads.includes(sectionId)) {
    reads.push(sectionId);
    saveToStorage(STORAGE_KEYS.READ_SECTIONS, reads);
  }
}

function isSectionRead(sectionId) {
  return getReadSections().includes(sectionId);
}

function getBookmarks() {
  return loadFromStorage(STORAGE_KEYS.BOOKMARKS, []);
}

function toggleBookmark(sectionId) {
  const bookmarks = getBookmarks();
  const idx = bookmarks.indexOf(sectionId);
  if (idx >= 0) {
    bookmarks.splice(idx, 1);
  } else {
    bookmarks.push(sectionId);
  }
  saveToStorage(STORAGE_KEYS.BOOKMARKS, bookmarks);
  return bookmarks.includes(sectionId);
}

function isBookmarked(sectionId) {
  return getBookmarks().includes(sectionId);
}

/* 查找小节数据 */
function findSection(sectionId) {
  for (const part of getCurrentBookData().parts) {
    for (const chapter of part.chapters) {
      for (const section of chapter.sections) {
        if (section.id === sectionId) {
          return { part, chapter, section };
        }
      }
    }
  }
  return null;
}

