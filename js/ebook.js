// 切换书
function switchBook(bookIndexStr) {
  const bookIndex = parseInt(bookIndexStr);
  if (bookIndex >= 0 && bookIndex < ALL_BOOKS.length && bookIndex !== currentBookIndex) {
    currentBookIndex = bookIndex;
    const book = getCurrentBookData();
    
    // 更新UI
    document.getElementById('ebookBookSelector').value = bookIndex;
    document.getElementById('ebookTitle').textContent = book.title;
    document.getElementById('ebookEdition').textContent = `${book.edition} · ${book.author}`;
    
    // 重新渲染目录，选择第一个章节
    renderTOC();
    const firstSection = getFirstSectionId();
    if (firstSection) {
      renderContent(firstSection);
    }
    
    // 保存选择
    localStorage.setItem('ebook_current_book', bookIndex);
  }
}

