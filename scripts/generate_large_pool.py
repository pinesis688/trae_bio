def main():
    pool = load_pool()
    existing_stems = {q.get("stem") for q in pool if isinstance(q, dict)}
    print(f"[start] 原有题目数: {len(pool)}")

    # 一、高考题：遍历每个原型，组合 stem × option_group 并随机化选项位置
    new_hs = 0
    for proto in GAOKAO_BANK:
        for stem in proto["stems"]:
            for opts_group in proto["items"]:
                opts_texts, correct, analysis = opts_group
                # 两次不同的位置重排（每次生成不同 q）
                for _ in range(2):
                    new_opts, new_ans = shuffle_options(opts_texts, correct)
                    q = make_q(
                        stem, new_opts, new_ans, analysis,
                        subject=proto["subject"], concept=proto["concept"],
                        module=proto["module"], difficulty="basic",
                        target="high_school",
                    )
                    if q["stem"] in existing_stems:
                        continue
                    existing_stems.add(q["stem"])
                    pool.append(q)
                    new_hs += 1
    print(f"[gaokao] 新增 {new_hs} 高考题")

    # 二、图表题
    new_chart = 0
    for c in CHART_BANK:
        opts_texts = c["opts"]
        correct = c["answer"]
        for _ in range(2):
            new_opts, new_ans = shuffle_options(opts_texts, correct)
            q = make_q(
                c["stem"], new_opts, new_ans, c["analysis"],
                subject=c["subject"], concept=c["concept"],
                module=c["module"], difficulty=c["difficulty"],
                target=c["target"], chart=c["chart"],
            )
            opts_tuple = tuple(new_opts)
            key = (c["stem"], str(new_ans), opts_tuple)
            existing_keys = set()
            for qq in pool:
                if isinstance(qq, dict) and qq.get("options") and isinstance(qq["options"], dict):
                    try:
                        existing_keys.add((qq["stem"], str(qq["answer"]), tuple(qq["options"].get(k, "") for k in "ABCD")))
                    except Exception:
                        pass
            if key in existing_keys:
                continue
            pool.append(q)
            new_chart += 1
    print(f"[chart] 新增 {new_chart} 图表题")

    # 三、竞赛题
    new_comp = 0
    for c in COMPETITION_BANK:
        opts_texts = c["opts"]
        correct = c["answer"]
        for _ in range(2):
            new_opts, new_ans = shuffle_options(opts_texts, correct)
            q = make_q(
                c["stem"], new_opts, new_ans, c["analysis"],
                subject=c["subject"], concept=c["concept"],
                module=c["module"], difficulty=c["difficulty"],
                target=c["target"], chart=c.get("chart"),
            )
            pool.append(q)
            new_comp += 1
    print(f"[competition] 新增 {new_comp} 竞赛题")

    # 去除重复（按 stem+answer 精确去重）
    seen = set()
    dedup = []
    for q in pool:
        if not isinstance(q, dict) or not q.get("stem"):
            continue
        opts = q.get("options")
        if isinstance(opts, dict):
            opts_tuple = tuple(opts.get(k, "") for k in "ABCD")
        else:
            opts_tuple = ()
        key = (q["stem"], str(q.get("answer")), opts_tuple)
        if key in seen:
            continue
        seen.add(key)
        dedup.append(q)

    save_pool(dedup)

    # 统计分布
    targets = {}
    diffs = {}
    modules = {}
    with_chart = 0
    for q in dedup:
        t = q.get("target") or "unknown"
        targets[t] = targets.get(t, 0) + 1
        d = str(q.get("difficulty", "?"))
        diffs[d] = diffs.get(d, 0) + 1
        m = q.get("module") or ""
        modules[m] = modules.get(m, 0) + 1
        if q.get("chart"):
            with_chart += 1
    print(f"[stats] targets={targets}")
    print(f"[stats] difficulties={diffs}")
    print(f"[stats] modules={modules}")
    print(f"[stats] with chart={with_chart}")


if __name__ == "__main__":
    main()
